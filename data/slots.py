"""
Доступные слоты для группового собеседования.

Кандидат, прошедший оба теста, получает выбор:
1. Один из этих слотов (групповое — могут выбрать несколько человек)
2. Кнопка «Время не подходит» — HR связывается лично

Чтобы изменить расписание — отредактируйте словарь INTERVIEW_SLOTS.
Ключ должен быть коротким (a-z, 0-9, без пробелов).
"""

INTERVIEW_SLOTS = {
    "mon_12": {
        "label": "Понедельник, 12:00–13:00",
        "short": "ПН 12:00",
    },
    "wed_09": {
        "label": "Среда, 09:00–10:00",
        "short": "СР 09:00",
    },
    "fri_15": {
        "label": "Пятница, 15:00–16:00",
        "short": "ПТ 15:00",
    },
}


def slot_label(slot_key: str) -> str:
    """Возвращает человекочитаемое название слота."""
    if slot_key == "no_match":
        return "Время не подходит (требуется индивидуальный подбор)"
    slot = INTERVIEW_SLOTS.get(slot_key)
    return slot["label"] if slot else slot_key
